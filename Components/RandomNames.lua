function randomNames.randomFirstName(side)
	return randomNames.firstNames[side][math.random(#randomNames.firstNames[side])]
end
function randomNames.randomLastName(side)
	return randomNames.lastNames[side][math.random(#randomNames.lastNames[side])]
end
function randomNames.getNewName(side)
	local name
	repeat
		name = randomNames.randomFirstName(side) .. " " .. randomNames.randomLastName(side)
	until not randomNames.used[name]
	randomNames.used[name] = true
	return name
end